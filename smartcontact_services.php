    <h2>Smart Contract</h2>
    <span>Smart contracts defined</span>
    <p>Smart contracts are simply programs stored on a blockchain that run when predetermined conditions are met. They
        typically are used to automate the execution of an agreement so that all participants can be immediately certain
        of the outcome, without any intermediary’s involvement or time loss. They can also automate a workflow,
        triggering the next action when conditions are met.
    </p>
    <img src="https://www.prochinafreight.com/wp-content/uploads/air-cargo-freight.jpg" width=700 alt="">
    <span>How smart contracts work</span>
    <p>Smart contracts work by following simple “if/when…then…” statements that are written into code on a blockchain. A
        network of computers executes the actions when predetermined conditions have been met and verified. These
        actions could include releasing funds to the appropriate parties, registering a vehicle, sending notifications,
        or issuing a ticket. The blockchain is then updated when the transaction is completed. That means the
        transaction cannot be changed, and only parties who have been granted permission can see the results.
        <br>
        Within a smart contract, there can be as many stipulations as needed to satisfy the participants that the task
        will be completed satisfactorily. To establish the terms, participants must determine how transactions and their
        data are represented on the blockchain, agree on the “if/when...then…” rules that govern those transactions,
        explore all possible exceptions, and define a framework for resolving disputes.
        <br>
        Then the smart contract can be programmed by a developer – although increasingly, organizations that use
        blockchain for business provide templates, web interfaces, and other online tools to simplify structuring smart
        contracts.
    </p>
    <span>Benefits of smart contracts</span>
    <p>
        <b>Speed, efficiency and accuracy</b><br>
        Once a condition is met, the contract is executed immediately. Because smart contracts are digital and
        automated, there’s no paperwork to process and no time spent reconciling errors that often result from manually
        filling in documents.
        <br>
        <b>Trust and transparency</b><br>
        Because there’s no third party involved, and because encrypted records of transactions are shared across
        participants, there’s no need to question whether information has been altered for personal benefit.
        <br><b>Security</b><br>
        Blockchain transaction records are encrypted, which makes them very hard to hack. Moreover, because each record
        is connected to the previous and subsequent records on a distributed ledger, hackers would have to alter the
        entire chain to change a single record.<br>
        <b>Savings</b><br>
        Smart contracts remove the need for intermediaries to handle transactions and, by extension, their associated
        time delays and fees.</p>
