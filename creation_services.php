        buy your NFT.
        Set a timed auction: A timed auction will give those interested in your NFT a time limit to submit their final
        bid.
        Start an unlimited auction: An unlimited auction doesn't set a time limit. Instead, you have control to end the
        auction whenever you want.
        <br>
