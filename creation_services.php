        transfer money from the buyer's wallet to yours. Fees also can fluctuate due to the volatility in cryptocurrency
        pricing. Because of that, it's important to take a close look at the costs you'll have to pay to make and sell
        your NFT to make sure they're worthwhile.
    </p>
