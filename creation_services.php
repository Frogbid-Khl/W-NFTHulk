    <span>1. Pick your item</span>
    <p>Let's start with the basics. If you haven't already done so, you'll need to determine what unique digital asset
        you want to turn into an NFT. It can be a custom painting, picture, music, video game collectible, meme, GIF, or
        even a tweet. An NFT is a unique digital item with a sole owner. That rarity gives an NFT value.
        <br>
        Make sure that you own the intellectual property rights to the item you want to turn into an NFT. Creating an
        NFT for a digital asset you don't own could get you into legal trouble.</p>

    <span>2. Choose your blockchain</span>
    <p>Once you've selected your unique digital asset, it's time to start the process of minting it into an NFT. That
        begins by determining the blockchain technology you intend to use for your NFT. The most popular among NFT
        artists and creators is Ethereum (CRYPTO:ETH). Other popular options include Tezos, Polkadot, Cosmos, and
        Binance Smart Chain.</p>

    <span>3. Set up your digital wallet</span>
    <p>
        If you don't already have a digital wallet, you'll want to set one up to create your NFT since you'll need some
        cryptocurrency to fund your initial investment. The wallet will provide you with access to your digital assets.
        The top NFT wallets include Metamask, Math Wallet, AlphaWallet, Trust Wallet, and Coinbase Wallet.
        <br>
        Once you set up your digital wallet, you'll want to buy some cryptocurrency. Most NFT platforms accept Ether,
        the cryptocurrency of the Ethereum blockchain platform. If you already own some cryptocurrency elsewhere, you'll
        want to connect it to your digital wallet so you can use it to create and sell NFTs.
    </p>
    <span>4. Select your NFT marketplace</span>
    <p>Once you have a digital wallet and some cryptocurrency, it's time to start creating (and, hopefully, selling)
        your NFT. For that, you'll need to choose an NFT marketplace. Some of the top NFT marketplaces include OpenSea,
        Axie Marketplace, Larva Labs/CryptoPunks, NBA Top Shot Marketplace, Rarible, SuperRare, Foundation, Nifty
        Gateway, Mintable, and ThetaDrop.
        <br>
        You'll need to research each NFT marketplace to find a platform that's a good fit for your NFT. For example,
        Axie Marketplace is the online shop for the top NFT game Axie Infinity. Meanwhile, NBA Top Shot is a
        basketball-focused marketplace. It's also important to note that some marketplaces require their own
        cryptocurrency. Rarible, for example, requires Rarible (CRYPTO:RARI).
        <br>
        OpenSea is usually a good place to start. It allows you to mint your own NFT, and it's a leader in NFT sales.
        The NFT marketplace sold $3.4 billion worth of NFTs in August 2021 alone.
        <br>
        After selecting your NFT marketplace, you'll need to connect it to your digital wallet. That will allow you to
        pay the necessary fees to mint your NFT and hold any sales proceeds.
    </p>
    <span>5. Upload your file</span>
    <p>
        You're now finally ready to mint your NFT. Your chosen NFT marketplace should have a step-by-step guide for uploading your digital file to their platform. That process will enable you to turn your digital file (a PNG, GIF, MP3, or other file type) into a marketable NFT.
    </p>
